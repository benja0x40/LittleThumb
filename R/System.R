# =============================================================================.
#' Current time
# -----------------------------------------------------------------------------.
#' @keywords internal
#' @export
clock <- function(x) {
  format(Sys.time(), '%H:%M:%S')
}

# =============================================================================.
#' Create a destination path
# -----------------------------------------------------------------------------.
#' @description
#' mkdir creates a destination path including upper level folders if necessary.
# -----------------------------------------------------------------------------.
#' @keywords internal
#' @export
mkdir <- function(x) {
  system(paste0('mkdir -p "', x, '"'))
}

# =============================================================================.
#' Make a symbolic link (Linux/macOS only)
# -----------------------------------------------------------------------------.
#' @keywords internal
#' @export
mklnk <- function(x, dest) {
  system(paste0('ln -fs "', dest, '" "', x, '"'))
}

# =============================================================================.
#' HostName
# -----------------------------------------------------------------------------.
#' @keywords internal
#' @export
HostName <- function() {
  as.character(Sys.info()["nodename"])
}

# =============================================================================.
#' SysName
# -----------------------------------------------------------------------------.
#' @keywords internal
#' @export
SysName <- function() {
  as.character(Sys.info()["sysname"])
}

# =============================================================================.
#' UserName
# -----------------------------------------------------------------------------.
#' @keywords internal
#' @export
UserName <- function() {
  as.character(Sys.info()["user"])
}

# =============================================================================.
#' HomePath
# -----------------------------------------------------------------------------.
#' @keywords internal
#' @export
HomePath <- function() {
  as.character(Sys.getenv()["HOME"])
}
